        public ActionResult SaveChanges(ApplicationUser user)
        {
            ApplicationUser uszver = null;
            if (ModelState.IsValid)
            {
                uszver = db.Users.Include(u => u.Discount).Where(u => u.UserName == User.Identity.Name).FirstOrDefault();
                if (UserManager.GetRoles(uszver.Id)[0] != "Driver")
                {
                    uszver.FIO = user.FIO;
                }
                Regex regexPhone = new Regex(@"[1-9]{3}-\d{3}-\d{2}-\d{2}$");
                if (user.PhoneNumber!=null && regexPhone.Match(user.PhoneNumber).Success)
                {
                    uszver.PhoneNumber = user.PhoneNumber;
                }
                else
                {
                    ViewBag.Error = "Номер должен соответствовать образцу";
                }
                db.SaveChanges();
            }
            return View("Edit",uszver);
        }
        public ActionResult Edit()
        {
            ApplicationUser uszver = db.Users.Include(u=>u.Discount).Where(u=>u.UserName==User.Identity.Name).FirstOrDefault();
            return View(uszver);
        }

        public ActionResult UserOrdersPartial(string navig = "", int page = 1)
        {
            string user = HttpContext.User.Identity.Name;
            if (user == "")
                return View("Index", "Home");
            int pageSize = 5;
            if (navig == "next")
            {
                page++;
            }
            else if (navig == "prev")
            {
                if (page != 1)
                    page--;
            }
            IEnumerable<Order> ordersPerPages = CONVERTER3000.TRANSFORMATION(db.Orders
                .Where(p => p.ApplicationUserID == (db.Users.Where(u => u.UserName == user).FirstOrDefault().Id))
                .Include(o => o.ApplicationUser)
                .Include(o => o.Driver)
                .OrderBy(o => o.OrderID)
                .ToList().Skip((page - 1) * pageSize).Take(pageSize).ToList());
            PageInfo pageInfo = new PageInfo { PageNumber = page, PageSize = pageSize, TotalItems = db.Orders.Where(o=>o.ApplicationUserID== (db.Users.Where(u => u.UserName == user).FirstOrDefault().Id)).Count() };
            if (ordersPerPages.Count() == 0)
            {
                page = pageInfo.TotalPages;
                ordersPerPages = CONVERTER3000.TRANSFORMATION(db.Orders
                .Where(p => p.ApplicationUserID == (db.Users.Where(u => u.UserName == user).FirstOrDefault().Id))
                .Include(o => o.ApplicationUser)
                .Include(o => o.Driver)
                .OrderBy(o => o.OrderID)
                .ToList().Skip((page - 1) * pageSize).Take(pageSize).ToList());
            }
            List<State> states = new List<State>();
            foreach (Order order in ordersPerPages)
            {
                State state = db.OrderStates.Where(o => o.OrderID == order.OrderID).OrderBy(o => o.date).ToList().Last().state;
                states.Add(state);
            }
            ViewModel viewModel = new ViewModel { PageInfo = pageInfo, Orders = ordersPerPages, States = states };
            ViewBag.NumberPage = page;
            ViewBag.TotalPages = pageInfo.TotalPages;
            return PartialView(viewModel);
        }



        public ActionResult Cancel(int id)
        {
            var order = db.Orders.Where(p => p.OrderID == id).FirstOrDefault();
            if (order.DriverID == null)
            {
                db.Orders.Remove(order);
                db.SaveChanges();
            }
            return RedirectToAction("Index", new { Message = ManageMessageId.Cancelled });
        }
